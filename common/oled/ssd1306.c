static void ssd1306_write_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd};
    i2c_write_blocking(I2C_INST, SSD1306_I2C_ADDR, buf, 2, false);
}

bool ssd1306_init(void) {
    // Initialize I2C
    i2c_init(I2C_INST, SSD1306_I2C_FREQ);
    gpio_set_function(SSD1306_SDA_PIN, GPIO_FUNC_I2C);
    gpio_set_function(SSD1306_SCL_PIN, GPIO_FUNC_I2C);
    gpio_pull_up(SSD1306_SDA_PIN);
    gpio_pull_up(SSD1306_SCL_PIN);
    
    printf("OLED: I2C initialized (SDA: %d, SCL: %d, ADDR: 0x%02X)\n", 
           SSD1306_SDA_PIN, SSD1306_SCL_PIN, SSD1306_I2C_ADDR);
    
    sleep_ms(100);
    
    // SSD1306 initialization sequence
    ssd1306_write_cmd(0xAE); // Display off
    ssd1306_write_cmd(0xD5); // Set display clock divide
    ssd1306_write_cmd(0x80);
    ssd1306_write_cmd(0xA8); // Set multiplex
    ssd1306_write_cmd(0x3F);
    ssd1306_write_cmd(0xD3); // Set display offset
    ssd1306_write_cmd(0x00);
    ssd1306_write_cmd(0x40); // Set start line
    ssd1306_write_cmd(0x8D); // Charge pump
    ssd1306_write_cmd(0x14);
    ssd1306_write_cmd(0x20); // Memory mode
    ssd1306_write_cmd(0x00);
    ssd1306_write_cmd(0xA1); // Segment remap
    ssd1306_write_cmd(0xC8); // COM scan dec
    ssd1306_write_cmd(0xDA); // Set COM pins
    ssd1306_write_cmd(0x12);
    ssd1306_write_cmd(0x81); // Set contrast
    ssd1306_write_cmd(0xCF);
    ssd1306_write_cmd(0xD9); // Set precharge
    ssd1306_write_cmd(0xF1);
    ssd1306_write_cmd(0xDB); // Set VCOM detect
    ssd1306_write_cmd(0x40);
    ssd1306_write_cmd(0xA4); // Display all on resume
    ssd1306_write_cmd(0xA6); // Normal display
    ssd1306_write_cmd(0xAF); // Display on
    
    ssd1306_clear();
    ssd1306_update();
    return true;
}

void ssd1306_clear(void) {
    memset(display_buffer, 0, sizeof(display_buffer));
}

void ssd1306_update(void) {
    // Set column address
    ssd1306_write_cmd(0x21);
    ssd1306_write_cmd(0);
    ssd1306_write_cmd(127);
    
    // Set page address
    ssd1306_write_cmd(0x22);
    ssd1306_write_cmd(0);
    ssd1306_write_cmd(7);
    
    // Send buffer in 16-byte chunks for reliability (matching working code)
    const int chunk_size = 16;
    for (size_t i = 0; i < sizeof(display_buffer); i += chunk_size) {
        size_t chunk = (i + chunk_size < sizeof(display_buffer)) ? chunk_size : sizeof(display_buffer) - i;
        
        uint8_t temp[17]; // 1 control byte + 16 data bytes
        temp[0] = 0x40;   // Data mode
        memcpy(temp + 1, display_buffer + i, chunk);
        
        i2c_write_blocking(I2C_INST, SSD1306_I2C_ADDR, temp, chunk + 1, false);
        sleep_us(100); // Small delay between chunks
    }
}

void ssd1306_write_line(const char* text, uint8_t line) {
    if (line >= 8) return;
    
    uint8_t x = 0;
    for (uint8_t i = 0; text[i] != '\0' && x < SSD1306_WIDTH - 6; i++) {
        char c = text[i];
        if (c < ' ' || c > 'z') c = ' ';  // Support space through lowercase z
        
        const uint8_t* glyph = font5x7[c - ' '];
        
        for (uint8_t col = 0; col < 5; col++) {
            uint16_t index = x + line * SSD1306_WIDTH;
            display_buffer[index] = glyph[col];
            x++;
        }
        x++; // Space between characters
    }
}

void ssd1306_set_pixel(uint8_t x, uint8_t y, bool on) {
    if (x >= SSD1306_WIDTH || y >= SSD1306_HEIGHT) return;
    
    uint16_t index = x + (y / 8) * SSD1306_WIDTH;
    uint8_t bit = y % 8;
    
    if (on) {
        display_buffer[index] |= (1 << bit);
    } else {
        display_buffer[index] &= ~(1 << bit);
    }
}

void ssd1306_draw_rect(uint8_t x, uint8_t y, uint8_t w, uint8_t h, bool filled) {
    for (uint8_t i = 0; i < w; i++) {
        for (uint8_t j = 0; j < h; j++) {
            if (filled || i == 0 || i == w-1 || j == 0 || j == h-1) {
                ssd1306_set_pixel(x + i, y + j, true);
            }
        }
    }
}

void ssd1306_i2c_scan(void) {
    printf("I2C Bus Scan: Scanning with 7-bit addresses...\n");
    
    for (uint8_t addr = 0; addr < 128; addr++) {
        uint8_t dummy;
        int ret = i2c_read_blocking(I2C_INST, addr, &dummy, 1, false);
        
        if (ret >= 0) {
            printf("I2C Bus Scan: Device found at address 0x%02X (7-bit) / 0x%02X (8-bit)\n", 
                   addr, addr << 1);
        }
    }
}
